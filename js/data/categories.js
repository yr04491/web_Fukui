// カテゴリ検索関数（AIの補助）
function findCategoriesByKeywords(text) {
    const foundCategories = new Set();
    const lowerText = text.toLowerCase();
    
    // キーワードマッピングを使用して候補を検索
    for (const [keyword, categories] of Object.entries(KEYWORD_MAPPING)) {
        if (lowerText.includes(keyword.toLowerCase())) {
            categories.forEach(cat => foundCategories.add(cat));
        }
    }
    
    // 直接的なカテゴリ名の検索
    ALL_CATEGORIES.forEach(category => {
        if (lowerText.includes(category.toLowerCase())) {
            foundCategories.add(category);
        }
    });
    
    return Array.from(foundCategories);
}

// ランダムカテゴリ選択（AIが判定できない場合の fallback）
function getRandomCategories(count = 3) {
    const shuffled = [...ALL_CATEGORIES].sort(() => 0.5 - Math.random());
    return shuffled.slice(0, count);
}

